import request from '@/utils/request'

const userApi = {
  Login: '/manage/account/login',
  Logout: '/auth/logout',
  ForgePassword: '/auth/forge-password',
  changePassword: '/manage/account/change',
  certificateKey: '/manage/account/certificate',
  Register: '/auth/register',
  twoStepCode: '/auth/2step-code',
  SendSms: '/account/sms',
  SendSmsErr: '/account/sms_err',
  // get my info
  UserInfo: '/user/info',
  UserMenu: '/user/nav'
}

/**
 * login func
 * parameter: {
 *     username: '',
 *     password: '',
 *     remember_me: true,
 *     captcha: '12345'
 * }
 * @param parameter
 * @returns {*}
 */
export function login (parameter) {
  return request({
    url: userApi.Login,
    method: 'post',
    data: {
      username: parameter.username,
      password: parameter.password
    }
  })
}

export function changePassword (parameter) {
  return request({
    url: userApi.changePassword,
    method: 'post',
    data: {
      password: parameter.password
    }
  })
}

export function certificateKey (parameter) {
  return request({
    url: userApi.certificateKey,
    method: 'post',
    data: {
      key: parameter.key
    }
  })
}

export function getSmsCaptcha (parameter) {
  return request({
    url: userApi.SendSms,
    method: 'post',
    data: parameter
  })
}

export function getInfo () {
  return request({
    url: userApi.UserInfo,
    method: 'get',
    headers: {
      'Content-Type': 'application/json;charset=UTF-8'
    }
  })
}

export function getCurrentUserNav () {
  return request({
    url: userApi.UserMenu,
    method: 'get'
  })
}

export function logout () {
  return request({
    url: userApi.Logout,
    method: 'post',
    headers: {
      'Content-Type': 'application/json;charset=UTF-8'
    }
  })
}

/**
 * get user 2step code open?
 * @param parameter {*}
 */
export function get2step (parameter) {
  return request({
    url: userApi.twoStepCode,
    method: 'post',
    data: parameter
  })
}
